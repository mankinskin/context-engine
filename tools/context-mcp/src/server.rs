//! MCP Server for context-engine hypergraph workspaces.
//!
//! Exposes the entire `context-api` command surface as a single MCP `execute`
//! tool. An MCP client sends a `Command` JSON object and receives a
//! `CommandResult` JSON object. The server runs over stdio transport using
//! `rmcp`, following the same pattern as `tools/log-viewer` and `tools/doc-viewer`.

use rmcp::{
    ErrorData as McpError,
    ServerHandler,
    ServiceExt,
    handler::server::{
        tool::ToolRouter,
        wrapper::Parameters,
    },
    model::*,
    schemars::{
        self,
        JsonSchema,
    },
    tool,
    tool_handler,
    tool_router,
    transport::stdio,
};
use serde::{
    Deserialize,
    Serialize,
};
use std::{
    path::PathBuf,
    sync::{
        Arc,
        Mutex,
    },
};

use context_api::{
    commands::{
        Command,
        CommandResult,
        execute,
    },
    workspace::manager::WorkspaceManager,
};

/// MCP Server for context-engine hypergraph operations.
///
/// Wraps a `WorkspaceManager` in `Arc<Mutex<>>` for interior mutability
/// (MCP tool handlers receive `&self` but `execute()` needs `&mut`).
/// The Mutex is appropriate because stdio MCP processes one tool call at a
/// time.
#[derive(Clone)]
pub struct ContextServer {
    /// Shared workspace manager.
    manager: Arc<Mutex<WorkspaceManager>>,
    tool_router: ToolRouter<Self>,
}

impl ContextServer {
    /// Create a new server rooted at the given base directory.
    ///
    /// Workspaces will be stored under `<base_dir>/.context-engine/`.
    pub fn new(base_dir: PathBuf) -> Self {
        let manager = WorkspaceManager::new(base_dir);
        Self {
            manager: Arc::new(Mutex::new(manager)),
            tool_router: Self::tool_router(),
        }
    }
}

// ---------------------------------------------------------------------------
// Tool input / output types
// ---------------------------------------------------------------------------

/// Input schema for the `execute` tool.
///
/// The `command` field is the full `Command` enum — its JSON schema is
/// auto-generated by schemars and documents every command variant and its
/// fields for the MCP client.
///
/// Because of `#[serde(flatten)]` the input JSON **is** the command itself
/// (no extra wrapping layer). For example:
///
/// ```json
/// {"command": "create_workspace", "name": "demo"}
/// ```
#[derive(Debug, Deserialize, JsonSchema)]
pub struct ExecuteInput {
    /// The command to execute. See the schema for all available commands.
    ///
    /// Examples:
    /// - `{"command": "create_workspace", "name": "my-graph"}`
    /// - `{"command": "add_atoms", "workspace": "my-graph", "chars": ["a","b","c"]}`
    /// - `{"command": "insert_sequence", "workspace": "my-graph", "text": "hello"}`
    /// - `{"command": "search_sequence", "workspace": "my-graph", "text": "hel"}`
    /// - `{"command": "read_as_text", "workspace": "my-graph", "index": 3}`
    /// - `{"command": "save_workspace", "name": "my-graph"}`
    #[serde(flatten)]
    pub command: Command,
}

/// Output wrapper for the `execute` tool.
///
/// API-level errors are returned as `success: false` with an `error` message
/// (not as MCP-level errors) so the agent can read and react to them.
#[derive(Debug, Serialize, Deserialize)]
pub struct ExecuteOutput {
    /// Whether the command succeeded.
    pub success: bool,
    /// The result (present when `success` is `true`).
    #[serde(skip_serializing_if = "Option::is_none")]
    pub result: Option<CommandResult>,
    /// Error message (present when `success` is `false`).
    #[serde(skip_serializing_if = "Option::is_none")]
    pub error: Option<String>,
}

// ---------------------------------------------------------------------------
// Tool implementation
// ---------------------------------------------------------------------------

#[tool_router]
impl ContextServer {
    /// Execute a context-engine command against a hypergraph workspace.
    ///
    /// This is the single entry point for all operations: workspace lifecycle,
    /// atom/pattern creation, search, insert, read, and debug commands.
    ///
    /// ## Workflow Example
    ///
    /// 1. Create a workspace: `{"command": "create_workspace", "name": "demo"}`
    /// 2. Add atoms: `{"command": "add_atoms", "workspace": "demo", "chars": ["a","b","c","d","e"]}`
    /// 3. Insert a sequence: `{"command": "insert_sequence", "workspace": "demo", "text": "abcde"}`
    /// 4. Search: `{"command": "search_sequence", "workspace": "demo", "text": "abc"}`
    /// 5. Read: `{"command": "read_as_text", "workspace": "demo", "index": 5}`
    /// 6. Save: `{"command": "save_workspace", "name": "demo"}`
    ///
    /// ## Available Commands
    ///
    /// **Workspace:** create_workspace, open_workspace, close_workspace,
    /// save_workspace, list_workspaces, delete_workspace
    ///
    /// **Atoms:** add_atom, add_atoms, get_atom, list_atoms
    ///
    /// **Patterns:** add_simple_pattern, get_vertex, list_vertices
    ///
    /// **Search:** search_pattern, search_sequence
    ///
    /// **Insert:** insert_first_match, insert_sequence, insert_sequences
    ///
    /// **Read:** read_pattern, read_as_text
    ///
    /// **Debug:** get_snapshot, get_statistics, validate_graph, show_graph, show_vertex
    #[tool(
        name = "execute",
        description = "Execute a context-engine hypergraph command. Send any Command JSON object and receive the corresponding CommandResult."
    )]
    async fn execute_command(
        &self,
        Parameters(input): Parameters<ExecuteInput>,
    ) -> Result<CallToolResult, McpError> {
        tracing::debug!(command = ?input.command, "Executing command");

        let result = {
            let mut manager = self.manager.lock().map_err(|e| {
                McpError::internal_error(format!("Lock poisoned: {e}"), None)
            })?;
            execute(&mut manager, input.command)
        };

        match result {
            Ok(cmd_result) => {
                let output = ExecuteOutput {
                    success: true,
                    result: Some(cmd_result),
                    error: None,
                };
                let json =
                    serde_json::to_string_pretty(&output).map_err(|e| {
                        McpError::internal_error(
                            format!("Serialization failed: {e}"),
                            None,
                        )
                    })?;
                Ok(CallToolResult::success(vec![Content::text(json)]))
            },
            Err(api_error) => {
                tracing::warn!(error = %api_error, "Command failed");
                let output = ExecuteOutput {
                    success: false,
                    result: None,
                    error: Some(format!("{api_error}")),
                };
                let json =
                    serde_json::to_string_pretty(&output).map_err(|e| {
                        McpError::internal_error(
                            format!("Serialization failed: {e}"),
                            None,
                        )
                    })?;
                // Return as a successful MCP response with an error payload
                // so the agent can read and react to the error message.
                Ok(CallToolResult::success(vec![Content::text(json)]))
            },
        }
    }
}

