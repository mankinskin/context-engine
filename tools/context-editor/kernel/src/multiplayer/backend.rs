/// Client-sent reducer requests (serialized and sent to SpacetimeDB).
#[derive(Clone, Debug)]
pub enum ReducerRequest {
    UpdateVoxel {
        x: i32,
        y: i32,
        z: i32,
        new_color: u32,
    },
    PlayerMove {
        position: (f32, f32, f32),
        yaw: f32,
    },
    PerformAttack {
        direction: (f32, f32, f32),
        weapon_slot: u8,
    },
    PickupItem {
        item_entity_id: u64,
    },
    DropItem {
        slot_index: u8,
    },
    Respawn {
        position: (f32, f32, f32),
    },
}

/// Server response for reducer calls.
#[derive(Clone, Debug)]
pub enum ReducerResponse {
    Success,
    Error(String),
}

// ---------------------------------------------------------------------------
// Client-side subscription state (Bevy resources)
// ---------------------------------------------------------------------------

/// Tracks our local player identity and connection status.
#[derive(Resource)]
pub struct MultiplayerConnection {
    pub identity: PlayerIdentity,
    pub connected: bool,
    pub server_url: String,
    /// Current server tick (incremented by subscription updates).
    pub server_tick: u64,
}

impl Default for MultiplayerConnection {
    fn default() -> Self {
        Self {
            identity: PlayerIdentity::local(),
            connected: false,
            server_url: String::new(),
            server_tick: 0,
        }
    }
}

/// Client-side cache of all players from the server subscription.
#[derive(Resource, Default)]
pub struct PlayerTable {
    pub players: HashMap<PlayerIdentity, PlayerRow>,
}

impl PlayerTable {
    /// Get the local player's row.
    pub fn local_player(
        &self,
        identity: &PlayerIdentity,
    ) -> Option<&PlayerRow> {
        self.players.get(identity)
    }

    /// Number of online players.
    pub fn online_count(&self) -> usize {
        self.players.len()
    }
}

/// Client-side cache of item blueprints from the server.
#[derive(Resource, Default)]
pub struct BlueprintTable {
    pub blueprints: HashMap<u32, ItemBlueprint>,
}

/// Client-side inventory state.
#[derive(Resource, Default)]
pub struct InventoryTable {
    pub slots: Vec<InventorySlot>,
}

impl InventoryTable {
    /// Get items in a specific slot.
    pub fn get_slot(
        &self,
        index: u8,
    ) -> Option<&InventorySlot> {
        self.slots.iter().find(|s| s.slot_index == index)
    }

    /// Count occupied slots.
    pub fn occupied_count(&self) -> usize {
        self.slots.iter().filter(|s| s.quantity > 0).count()
    }
}

/// Queue of pending reducer requests to send to the server.
#[derive(Resource, Default)]
pub struct ReducerQueue {
    pub pending: Vec<ReducerRequest>,
}

impl ReducerQueue {
    pub fn push(
        &mut self,
        req: ReducerRequest,
    ) {
        self.pending.push(req);
    }

    pub fn drain(&mut self) -> Vec<ReducerRequest> {
        std::mem::take(&mut self.pending)
    }
}

/// Incoming combat events from server subscription.
#[derive(Resource, Default)]
pub struct CombatEventQueue {
    pub events: Vec<CombatEvent>,
}

// ---------------------------------------------------------------------------
// Validation helpers (client-side mirrors of server logic)
// ---------------------------------------------------------------------------

/// Validate that a player is within interaction range of a target position.
pub fn validate_interaction_range(
    player_pos: (f32, f32, f32),
    target: (f32, f32, f32),
) -> bool {
    let dx = player_pos.0 - target.0;
    let dy = player_pos.1 - target.1;
    let dz = player_pos.2 - target.2;
    (dx * dx + dy * dy + dz * dz).sqrt() <= MAX_INTERACTION_RANGE
}

/// Compute damage from weapon stats against a target.
pub fn compute_damage(
    base_damage: i32,
    weapon_weight: f32,
) -> i32 {
    let weight_bonus = (weapon_weight * 2.0) as i32;
    (base_damage + weight_bonus).max(1)
}

// ---------------------------------------------------------------------------
// Offline/singleplayer game tick
// ---------------------------------------------------------------------------

/// Tracks the local game tick for singleplayer fallback.
#[derive(Resource)]
pub struct LocalGameTick {
    pub tick: u64,
    pub timer_ms: f64,
}

impl Default for LocalGameTick {
    fn default() -> Self {
        Self {
            tick: 0,
            timer_ms: 0.0,
        }
    }
}

/// System: advance the local game tick at TICK_INTERVAL_MS when offline.
fn local_tick_system(
    time: Res<Time>,
    connection: Res<MultiplayerConnection>,
    mut tick: ResMut<LocalGameTick>,
) {
    if connection.connected {
        return; // Server drives the tick when online
    }

    tick.timer_ms += time.delta_secs_f64() * 1000.0;
    while tick.timer_ms >= TICK_INTERVAL_MS as f64 {
        tick.timer_ms -= TICK_INTERVAL_MS as f64;
        tick.tick += 1;
    }
}

/// System: drain the reducer queue (in singleplayer mode, apply locally).
fn drain_reducer_queue_system(
    connection: Res<MultiplayerConnection>,
    mut queue: ResMut<ReducerQueue>,
    mut players: ResMut<PlayerTable>,
) {
    if connection.connected {
        // In connected mode, a network transport system would send these
        // to SpacetimeDB. For now we just clear them.
        queue.pending.clear();
        return;
    }

    // Singleplayer: apply locally
    let requests = queue.drain();
    for req in requests {
        match req {
            ReducerRequest::PlayerMove { position, yaw } => {
                if let Some(player) =
                    players.players.get_mut(&connection.identity)
                {
                    player.position = position;
                    player.rotation_yaw = yaw;
                }
            },
            // Other reducers would need SVO access etc. — stubbed for now.
            _ => {},
        }
    }
}

// ---------------------------------------------------------------------------
// Plugin
// ---------------------------------------------------------------------------

/// Plugin registering multiplayer backend types and fallback tick systems.
pub struct MultiplayerBackendPlugin;

impl Plugin for MultiplayerBackendPlugin {
    fn build(
        &self,
        app: &mut App,
    ) {
        app.init_resource::<MultiplayerConnection>();
        app.init_resource::<PlayerTable>();
        app.init_resource::<BlueprintTable>();
        app.init_resource::<InventoryTable>();
        app.init_resource::<ReducerQueue>();
        app.init_resource::<CombatEventQueue>();
        app.init_resource::<LocalGameTick>();

        // Insert a default local player row
        let identity = PlayerIdentity::local();
        let mut player_table = PlayerTable::default();
        player_table.players.insert(identity, PlayerRow::default());
        app.insert_resource(player_table);

        app.add_systems(
            Update,
            (local_tick_system, drain_reducer_queue_system),
        );
    }
}

