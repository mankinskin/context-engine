//! Multiplayer Backend: shared types, table definitions, reducers, and auth.
//!
//! This module provides the client-side representation of the SpacetimeDB
//! server module. Types here mirror the server tables and reducer signatures
//! so the WASM client can serialize/deserialize multiplayer state.
//!
//! The actual SpacetimeDB server module lives in `tools/context-editor/server/`
//! and is compiled separately. This kernel module handles:
//! - Shared table types (Player, VoxelChunk, VoxelDelta, etc.)
//! - Client-side subscription state
//! - Reducer request/response types
//! - Auth identity management
//! - Game tick simulation for offline/singleplayer fallback

use bevy::prelude::*;
use std::collections::HashMap;

// ---------------------------------------------------------------------------
// Constants
// ---------------------------------------------------------------------------

/// Maximum interaction range for voxel editing (world units).
pub const MAX_INTERACTION_RANGE: f32 = 10.0;

/// Chunk size in voxels per axis (16³ chunks).
pub const CHUNK_SIZE: u32 = 16;

/// Game tick interval in milliseconds.
pub const TICK_INTERVAL_MS: u64 = 50;

/// Maximum inventory slots per player.
pub const MAX_INVENTORY_SLOTS: u8 = 40;

// ---------------------------------------------------------------------------
// Identity
// ---------------------------------------------------------------------------

/// Cryptographic player identity (mirrors SpacetimeDB `Identity`).
#[derive(Clone, Copy, Debug, PartialEq, Eq, Hash)]
pub struct PlayerIdentity {
    /// 128-bit identity hash.
    pub id: u128,
}

impl PlayerIdentity {
    pub fn new(id: u128) -> Self {
        Self { id }
    }

    /// Generate a local-only identity for singleplayer mode.
    pub fn local() -> Self {
        Self { id: 1 }
    }
}

// ---------------------------------------------------------------------------
// Table types (shared between client and server)
// ---------------------------------------------------------------------------

/// A 16³ voxel chunk stored in the server database.
#[derive(Clone, Debug)]
pub struct VoxelChunk {
    /// Spatial hash from (cx, cy, cz).
    pub chunk_hash: u64,
    pub cx: i32,
    pub cy: i32,
    pub cz: i32,
    /// Serialized `OctreeNode` array for this chunk.
    pub octree_data: Vec<u8>,
    /// Tick number of last modification.
    pub last_modified: u64,
}

/// A single-voxel delta applied to a chunk.
#[derive(Clone, Debug)]
pub struct VoxelDelta {
    pub delta_id: u64,
    pub chunk_hash: u64,
    pub local_x: u8,
    pub local_y: u8,
    pub local_z: u8,
    pub new_color: u32,
    pub tick: u64,
}

/// Player state stored in the server database.
#[derive(Clone, Debug)]
pub struct PlayerRow {
    pub identity: PlayerIdentity,
    pub entity_id: u64,
    pub position: (f32, f32, f32),
    pub velocity: (f32, f32, f32),
    pub rotation_yaw: f32,
    pub hp: i32,
    pub max_hp: i32,
    pub mana: i32,
    pub max_mana: i32,
    pub skin_color: u32,
    pub last_tick: u64,
}

impl Default for PlayerRow {
    fn default() -> Self {
        Self {
            identity: PlayerIdentity::local(),
            entity_id: 0,
            position: (0.0, 10.0, 0.0),
            velocity: (0.0, 0.0, 0.0),
            rotation_yaw: 0.0,
            hp: 100,
            max_hp: 100,
            mana: 50,
            max_mana: 50,
            skin_color: 0xFFCCAA,
            last_tick: 0,
        }
    }
}

/// Item blueprint defining a craftable/equippable item.
#[derive(Clone, Debug)]
pub struct ItemBlueprint {
    pub blueprint_id: u32,
    pub name: String,
    /// Serialized 8³ mini-SVO for item appearance.
    pub voxel_data: Vec<u8>,
    pub base_damage: i32,
    pub weight: f32,
    pub item_type: ItemType,
}

/// Type classification for items.
#[derive(Clone, Copy, Debug, PartialEq, Eq)]
#[repr(u8)]
pub enum ItemType {
    Weapon = 0,
    Tool = 1,
    Block = 2,
    Consumable = 3,
}

impl ItemType {
    pub fn from_u8(v: u8) -> Self {
        match v {
            0 => Self::Weapon,
            1 => Self::Tool,
            2 => Self::Block,
            3 => Self::Consumable,
            _ => Self::Block,
        }
    }
}

/// A slot in a player's inventory.
#[derive(Clone, Debug)]
pub struct InventorySlot {
    pub slot_id: u64,
    pub owner: PlayerIdentity,
    pub blueprint_id: u32,
    pub quantity: u32,
    pub slot_index: u8,
}

/// Combat event generated by the server when an attack lands.
#[derive(Clone, Debug)]
pub struct CombatEvent {
    pub event_id: u64,
    pub attacker: PlayerIdentity,
    pub target: PlayerIdentity,
    pub damage: i32,
    pub hit_pos: (f32, f32, f32),
    pub tick: u64,
}

// ---------------------------------------------------------------------------
// Spatial hashing
// ---------------------------------------------------------------------------

/// Compute a spatial hash for chunk coordinates.
///
/// Uses FNV-1a–style mixing to distribute chunks across hash space.
pub fn spatial_hash(
    cx: i32,
    cy: i32,
    cz: i32,
) -> u64 {
    let mut h: u64 = 0xcbf29ce484222325;
    for byte in cx
        .to_le_bytes()
        .iter()
        .chain(&cy.to_le_bytes())
        .chain(&cz.to_le_bytes())
    {
        h ^= *byte as u64;
        h = h.wrapping_mul(0x100000001b3);
    }
    h
}

/// Convert world voxel coordinates to chunk coordinates.
pub fn world_to_chunk(
    x: i32,
    y: i32,
    z: i32,
) -> (i32, i32, i32) {
    (
        x.div_euclid(CHUNK_SIZE as i32),
        y.div_euclid(CHUNK_SIZE as i32),
        z.div_euclid(CHUNK_SIZE as i32),
    )
}

/// Convert world voxel coordinates to local-within-chunk coordinates.
pub fn world_to_local(
    x: i32,
    y: i32,
    z: i32,
) -> (u8, u8, u8) {
    (
        x.rem_euclid(CHUNK_SIZE as i32) as u8,
        y.rem_euclid(CHUNK_SIZE as i32) as u8,
        z.rem_euclid(CHUNK_SIZE as i32) as u8,
    )
}

// ---------------------------------------------------------------------------
// Reducer request types
